import type { FieldDefinition, ObjectDefinition, RelationshipDefinition } from "./types.js";

type FieldOptions = Partial<Omit<FieldDefinition, "name" | "type">>;

const field = (
  name: string,
  type: FieldDefinition["type"],
  options: FieldOptions = {},
): FieldDefinition => ({
  name,
  type,
  required: options.required ?? false,
  unique: options.unique ?? false,
  maxLength: options.maxLength ?? null,
  lookup: options.lookup ?? null,
  validation: options.validation ?? null,
  classification: options.classification ?? "internal",
  aiReadable: options.aiReadable ?? false,
  aiWritable: options.aiWritable ?? false,
  searchable: options.searchable ?? false,
  apiExposed: options.apiExposed ?? false,
});

const relationship = (
  name: string,
  target: string,
  cardinality: RelationshipDefinition["cardinality"],
  cascade = false,
): RelationshipDefinition => ({ name, target, cardinality, cascade });

